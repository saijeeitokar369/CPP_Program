// Data Type Sizes
#include<iostream>
using namespace std;
int main(){
    cout<<"Size of double:"<<sizeof(double)<<"byte(s)"<<"\n";
    cout<<"Size of float:"<<sizeof(float)<<"byte(s)"<<"\n";
    cout<<"Size of int:"<<sizeof(int)<<"byte(s)"<<"\n";
    cout<<"Size of char:"<<sizeof(char)<<"byte(s)";
}